public class Codigo05 {
    public static void main(String[] args) {
        System.out.println();

        System.out.print("Um caractere: \t\t\t");
        System.out.print('A');
        System.out.print("\n");

        System.out.print("Um número inteiro:\t\t");
        System.out.print(123);
        System.out.print("\n");

        System.out.println("Um número decimal:\t\t" + 4.3);
        System.out.println("Um valor boleano:\t\t" + true);
        System.out.println("Uma barra invertida:\t\\");
        System.out.println("Um texto entre aspas:\t" + "\"ENTRE ASPAS\"");

        System.out.println();
    }
}
