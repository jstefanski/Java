public class Codigo06 {
    public static void main(String[] args) {
        System.out.println();
        System.out.println("Inteiro - Decimal:\t\t\t" + 11);
        System.out.println("Inteiro - Hexadecimal:\t\t" + 0xB);
        System.out.println("Inteiro - Octal:\t\t\t" + 013);
        System.out.println("Inteiro - Longo:\t\t\t" + 11L);
        System.out.println("Real - Precisão Simples:\t" + 24.2f);
        System.out.println("Real - Precisão Dupla:\t\t" + 24.2);
        System.out.println("Tipo Lógico:\t\t\t\t" + true);
        System.out.println("Caractere:\t\t\t\t\t" + 'A');
        System.out.println("Texto:\t\t\t\t\t\t" + "Nome Completo");
        System.out.println();
    }
}
