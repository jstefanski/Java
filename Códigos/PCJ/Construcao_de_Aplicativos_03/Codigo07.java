package Construcao_de_Aplicativos_03.Codigos;

public class Codigo07 {
    public static void main(String[] args) {
        System.out.println();

        System.out.printf("Um caractere: %c", 'A'); //c - caractere
        System.out.println();
        System.out.printf("Um texto: %s", "Aprendizado em Java!!"); //%s - texto
        System.out.println();
        System.out.printf("Um número inteiro: %d", 12345); //%d - número inteiro
        System.out.println();
        System.out.printf("Um número decimal: %f", 1.99); //%f - número decimal
        System.out.println();
        System.out.printf("Um valor lógico: %b", true); //%b - valor booleano
        System.out.println();
    }
}
